// Récupérer un film par son id
export function getFilmById(id) {
  return films.find(f => f.id === id);
}

// Vérifier la réponse du joueur (insensible à la casse)
export function checkAnswer(id, reponse) {
  const film = getFilmById(id);
  if (!film) return false;
  return film.title.toLowerCase().trim() === reponse.toLowerCase().trim();
}

// Récupérer tous les films d'une zone
export function getFilmsByZone(zone) {
  return films.filter(f => f.zone === zone);
}

// Récupérer les films par difficulté
export function getFilmsByDifficulty(difficulty) {
  return films.filter(f => f.difficulty === difficulty);
}